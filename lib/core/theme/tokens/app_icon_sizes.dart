/// Icon size scale (logical pixels).
abstract final class AppIconSizes {
  /// 12 — inline text decorators.
  static const double xxs = 12;

  /// 14
  static const double xs = 14;

  /// 16 — dense lists, badges.
  static const double sm = 16;

  /// 20 — default inline icon.
  static const double md = 20;

  /// 24 — app bars, nav bars, default buttons.
  static const double lg = 24;

  /// 32 — large actions, empty state.
  static const double xl = 32;

  /// 40
  static const double xxl = 40;

  /// 48 — hero / empty-state illustrations.
  static const double xxxl = 48;

  /// 64
  static const double massive = 64;
}
